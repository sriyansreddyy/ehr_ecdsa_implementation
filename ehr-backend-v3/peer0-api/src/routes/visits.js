'use strict';

const router = require('express').Router();
const { body, param, validationResult } = require('express-validator');
const { authenticate, requireRole, requireAnyKnownRole } = require('../middleware/auth');
const { peerContext }  = require('../middleware/peerContext');
const { wrap, parseResult } = require('../middleware/errorHandler');
const { initVisit, fetchByCID, pinJSON } = require('../fabric/ipfsClient');
const logger = require('../config/logger');

const canManage    = [authenticate, requireRole('receptionist', 'admin'), peerContext];
const canDischarge = [authenticate, requireRole('admin'), peerContext];
const canRead      = [authenticate, requireAnyKnownRole, peerContext];

// ── GET /visits — ListAllVisits ───────────────────────────────
router.get('/', ...canRead, wrap(async (req, res) => {
  const result = await req.contract.evaluateTransaction('VisitContract:ListAllVisits');
  return res.json({ success: true, data: parseResult(result) });
}));

// ── POST /visits — OpenVisit ──────────────────────────────────
// 1. Pin empty visit JSON to IPFS (gets visitId from patient's visitCount+1)
//    Problem: visitId is generated by chaincode. So we must derive it first.
//    Solution: read patient visitCount, derive visitId, pin, then submit.
router.post('/',
  ...canManage,
  [
    body('patientId').trim().notEmpty().withMessage('patientId required'),
    body('chiefComplaint').optional().isString(),
  ],
  wrap(async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) return res.status(400).json({ success: false, errors: errors.array() });

    const { patientId, chiefComplaint = '' } = req.body;
    logger.info('OpenVisit', { patientId, userId: req.user.userId });

    // Step 1 — get patient's current visitCount to derive the new visitId
    const patientResult = await req.contract.evaluateTransaction(
      'PatientContract:GetPatient', patientId
    );
    const patient = parseResult(patientResult);
    if (!patient) return res.status(404).json({ success: false, error: `Patient not found: ${patientId}` });

    const visitNumber = (patient.visitCount || 0) + 1;
    const visitId = `${patientId}-V${visitNumber}`;

    // Step 2 — pin empty visit JSON to IPFS
    const { cid: visitCID } = await initVisit(visitId, patientId, chiefComplaint, req.user.userId);
    logger.info('Visit JSON pinned to IPFS', { visitId, visitCID });

    // Step 3 — open visit on blockchain (pass visitCID)
    const result = await req.contract.submitTransaction(
      'VisitContract:OpenVisit', patientId, chiefComplaint, visitCID
    );
    return res.status(201).json({ success: true, data: parseResult(result) });
  })
);

