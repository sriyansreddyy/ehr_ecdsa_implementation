// ── GET /visits/:id/ipfs — full IPFS content ─────────────────
// Fetch the actual clinical JSON from IPFS for a visit
router.get('/:id/ipfs',
  ...canRead,
  wrap(async (req, res) => {
    const visitResult = await req.contract.evaluateTransaction(
      'VisitContract:GetVisit', req.params.id
    );
    const visit = parseResult(visitResult);
    if (!visit) return res.status(404).json({ success: false, error: 'Visit not found' });
    if (!visit.visitCID) return res.json({ success: true, data: null });
    const clinical = await fetchByCID(visit.visitCID);
    return res.json({ success: true, data: { ...visit, clinical } });
  })
);

// ── PUT /visits/:id/doctor — AssignDoctor ─────────────────────
// Fetch visit JSON → add forwardingLog entry → repin → AssignDoctor(visitCID)
router.put('/:id/doctor',
  ...canManage,
  [
    param('id').trim().notEmpty(),
    body('doctorId').trim().notEmpty().withMessage('doctorId required'),
  ],
  wrap(async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) return res.status(400).json({ success: false, errors: errors.array() });

    const visitId = req.params.id;
    const { doctorId } = req.body;
    logger.info('AssignDoctor', { visitId, doctorId, userId: req.user.userId });

    // Fetch current visit JSON and update forwardingLog
    const visitOnChain = parseResult(await req.contract.evaluateTransaction('VisitContract:GetVisit', visitId));
    const visitJson = await fetchByCID(visitOnChain.visitCID);
    visitJson.assignedDoctor = doctorId;
    visitJson.forwardingLog.push({
      action: 'DOCTOR_ASSIGNED', from: req.user.userId, fromRole: req.user.role,
      to: doctorId, toRole: 'doctor', notes: '', timestamp: new Date().toISOString(),
    });
    visitJson.updatedAt = new Date().toISOString();
    const newCID = await pinJSON(visitJson, `visit-${visitId}-doctor.json`);

    const result = await req.contract.submitTransaction(
      'VisitContract:AssignDoctor', visitId, doctorId, newCID
    );
    return res.json({ success: true, data: parseResult(result) });
  })
);

